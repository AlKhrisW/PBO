package PPertemuan4;

public class Dosen {
    private String kodeDosen;
    private String namaDosen;
    public Dosen(String kodeDosen, String namaDosen) {
        this.kodeDosen = kodeDosen;
        this.namaDosen = namaDosen;
    }
    public String getKodeDosen() {
        return kodeDosen;
    }
    public String getNamaDosen() {
        return namaDosen;
    }
}
